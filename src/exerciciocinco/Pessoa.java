package exerciciocinco;

public class Pessoa {
		private String nome;
		private char sexo;
		
		
		public void setNome(String nome) {
			this.nome = nome;
		}
		
		public void setSexo(char sexo) {
			this.sexo = sexo;
		}
		
		public String getNome() {
			return nome;
		}
		
		public char getSexo() {
			return sexo;
		}
		
}
